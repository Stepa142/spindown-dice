const itemArray = Object.keys(items);
const itemCount = itemArray.length;

document.getElementById('findButton').addEventListener('click', function() {
    const itemName = document.getElementById('itemInput').value;
    const itemId = items[itemName];

    if (itemId) {
        const negativeIds = [-1, -2, -3].map(offset => {
            const newId = itemId + offset;
            // Ensure the newId is within the bounds of the itemArray
            return itemArray[(newId - 1 + itemCount) % itemCount]; // Adjust for 0-based index
        });

        document.getElementById('result').innerHTML = `
            <p>Item Name -1: ${negativeIds[0]}</p>
            <p>Item Name -2: ${negativeIds[1]}</p>
            <p>Item Name -3: ${negativeIds[2]}</p>
        `;
    } else {
        document.getElementById('result').innerHTML = '<p>Item not found!</p>';
    }
});

// Autocomplete functionality
document.getElementById('itemInput').addEventListener('input', function() {
    const value = this.value;
    const autocompleteList = document.getElementById('autocomplete-list');
    autocompleteList.innerHTML = ''; // Clear previous suggestions

    if (!value) return; // Exit if input is empty

    const filteredItems = itemArray.filter(item => item.toLowerCase().includes(value.toLowerCase()));

    filteredItems.forEach(item => {
        const suggestion = document.createElement('div');
        suggestion.innerHTML = item;
        suggestion.addEventListener('click', function() {
            document.getElementById('itemInput').value = item; // Set input value to the selected item
            autocompleteList.innerHTML = ''; // Clear suggestions
        });
        autocompleteList.appendChild(suggestion);
    });
});